/**
 * Search the static asset universe by case-insensitive substring on the ticker. Returns the
 * CoinSearchResult shape the autocomplete expects; with ticker-only labels, name === symbol.
 */
export function searchAssetList(query: string, limit = 10): CoinSearchResult[] {
  const q = query.trim().toUpperCase();
  if (q.length === 0) return [];
  const out: CoinSearchResult[] = [];
  for (const ticker of ASSET_TICKERS) {
    if (ticker.includes(q)) {
      out.push({ id: ticker, name: ticker, symbol: ticker, rank: 0, is_active: true });
      if (out.length >= limit) break;
    }
  }
  return out;
}
